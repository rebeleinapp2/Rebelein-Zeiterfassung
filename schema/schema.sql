CREATE FUNCTION public.handle_entry_history_response(p_entry_id uuid, p_action text, p_note text DEFAULT NULL::text) RETURNS void
    LANGUAGE plpgsql SECURITY DEFINER
    AS $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM time_entries 
        WHERE id = p_entry_id AND user_id = auth.uid()
    ) THEN
        RAISE EXCEPTION 'Not authorized';
    END IF;
    IF p_action = 'confirm' THEN
        UPDATE entry_change_history
        SET status = 'confirmed',
            user_response_at = NOW()
        WHERE entry_id = p_entry_id AND status = 'pending';
        
        UPDATE time_entries
        SET change_confirmed_by_user = true
        WHERE id = p_entry_id;
        
    ELSIF p_action = 'reject' THEN
        UPDATE entry_change_history
        SET status = 'rejected',
            user_response_at = NOW(),
            user_response_note = p_note
        WHERE entry_id = p_entry_id AND status = 'pending';
        
        UPDATE time_entries
        SET change_confirmed_by_user = true
        WHERE id = p_entry_id;
    END IF;
END;
$$;


