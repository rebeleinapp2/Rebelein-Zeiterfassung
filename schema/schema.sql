--
-- Name: view_daily_summary; Type: VIEW; Schema: public; Owner: postgres
--

CREATE VIEW public.view_daily_summary AS
 SELECT user_id,
    date,
    (sum(
        CASE
            WHEN (type = ANY (ARRAY['work'::text, 'company'::text, 'office'::text, 'warehouse'::text, 'car'::text, 'emergency_service'::text])) THEN calc_duration_minutes
            ELSE 0
        END) - sum(
        CASE
            WHEN (type = 'break'::text) THEN calc_duration_minutes
            ELSE 0
        END)) AS total_work_minutes,
    ((sum(
        CASE
            WHEN (type = 'vacation'::text) THEN calc_duration_minutes
            ELSE 0
        END))::numeric / 60.0) AS vacation_hours,
    ((sum(
        CASE
            WHEN (type = ANY (ARRAY['sick'::text, 'sick_pay'::text, 'child_sick'::text])) THEN calc_duration_minutes
            ELSE 0
        END))::numeric / 60.0) AS sick_hours,
    ((sum(
        CASE
            WHEN (type = 'holiday'::text) THEN calc_duration_minutes
            ELSE 0
        END))::numeric / 60.0) AS holiday_hours,
    sum(calc_surcharge_hours) AS total_surcharge_hours,
    ((sum(
        CASE
            WHEN (type = ANY (ARRAY['work'::text, 'company'::text, 'office'::text, 'warehouse'::text, 'car'::text, 'emergency_service'::text])) THEN calc_duration_minutes
            ELSE 0
        END) - sum(
        CASE
            WHEN (type = 'break'::text) THEN calc_duration_minutes
            ELSE 0
        END)) + ((sum(calc_surcharge_hours) * (60)::numeric))::integer) AS total_effective_minutes
   FROM public.time_entries
  WHERE ((is_deleted IS NULL) OR (is_deleted = false))
  GROUP BY user_id, date;


